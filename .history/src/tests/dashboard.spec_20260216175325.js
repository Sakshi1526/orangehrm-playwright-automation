import { test, expect } from '@playwright/test';

test('test', async ({ page }) => {
  await page.goto('https://opensource-demo.orangehrmlive.com/web/index.php/auth/login');
  await page.getByRole('textbox', { name: 'Username' }).click();
  await page.getByRole('textbox', { name: 'Username' }).fill('Admin');
  await page.getByRole('textbox', { name: 'Password' }).click();
  await page.getByRole('textbox', { name: 'Password' }).fill('admin123');
  await page.getByRole('button', { name: 'Login' }).click();
  await page.goto('https://opensource-demo.orangehrmlive.com/web/index.php/dashboard/index');
  await expect(page.getByRole('link', { name: 'Admin' })).toBeVisible();
  await page.getByRole('link', { name: 'Admin' }).click();
  await page.getByRole('textbox').nth(1).click();
  await page.getByRole('textbox').nth(1).fill('Admin');
  await expect(page.locator('.oxd-icon.bi-caret-down-fill.oxd-select-text--arrow').first()).toBeVisible();
  await page.locator('.oxd-icon.bi-caret-down-fill.oxd-select-text--arrow').first().click();
  await page.getByRole('listbox').getByText('Admin').click();
  await page.getByRole('button', { name: 'Search' }).click();
  await page.locator('.oxd-icon.bi-check').first().click();
  await expect(page.getByRole('link', { name: 'Recruitment' })).toBeVisible();
  await page.getByRole('link', { name: 'Recruitment' }).click();
  await page.goto('https://opensource-demo.orangehrmlive.com/web/index.php/recruitment/viewCandidates');
  await expect(page.getByRole('link', { name: 'Vacancies' })).toBeVisible();
  await page.getByRole('link', { name: 'Vacancies' }).click();
  await expect(page.locator('.oxd-icon.bi-caret-down-fill.oxd-select-text--arrow').first()).toBeVisible();
  await page.locator('.oxd-icon.bi-caret-down-fill.oxd-select-text--arrow').first().click();
  await page.getByRole('listbox').getByText('Account Assistant').click();
  await expect(page.locator('div:nth-child(2) > .oxd-input-group > div:nth-child(2) > .oxd-select-wrapper > .oxd-select-text > .oxd-select-text--after > .oxd-icon')).toBeVisible();
  await page.locator('div:nth-child(2) > .oxd-input-group > div:nth-child(2) > .oxd-select-wrapper > .oxd-select-text > .oxd-select-text--after > .oxd-icon').click();
  await page.getByRole('listbox').getByText('Junior Account Assistant').click();
  await page.getByRole('button', { name: 'Search' }).click();
  await page.locator('.oxd-icon.bi-check').first().click();
  await expect(page.getByRole('link', { name: 'Dashboard' })).toBeVisible();
  await page.getByRole('link', { name: 'Dashboard' }).click();
  await page.getByRole('link', { name: 'Dashboard' }).click();
  await page.goto('https://opensource-demo.orangehrmlive.com/web/index.php/dashboard/index');
});